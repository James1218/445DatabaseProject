    private void addMealPlanJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMealPlanJButtonActionPerformed
        
        StringBuilder sb = new StringBuilder();
        String foodType = mealTypeComboBox.getSelectedItem().toString();
        String foodName = foodComboBox.getSelectedItem().toString();
        String date = dateJComboBox.getSelectedItem().toString();
        
        if(foodType != null && foodName != null && date != null && accountName != null) {
            DatabaseAccess.insertMealPlan(date, foodName, foodType, accountName);
            JOptionPane.showMessageDialog(null, "Meal plan updated!");
            sb.append("Current meal plan : \n----------------------------------\n");
            sb.append(DatabaseAccess.getMealPlan(accountName));
            meauPlanJTextArea.setText(sb.toString());
        } else {
            JOptionPane.showMessageDialog(null, "Meal plan did not updated!");
        }
    }//GEN-LAST:event_addMealPlanJButtonActionPerformed

