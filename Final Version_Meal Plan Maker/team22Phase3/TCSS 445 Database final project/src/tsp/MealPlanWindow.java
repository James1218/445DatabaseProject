    public void fillInFoodComboBox () {
        
        List<String> foodList = DatabaseAccess.getTheFoodList();
        
        for (String foodName : foodList) {
            foodComboBox.addItem(foodName);
        }
    } 
    
    public void fillInMeauTypeComboBox() {
        
        List<String> foodTypeList = DatabaseAccess.getTheFoodTypeList();
        
        for (String foodType : foodTypeList) {
            mealTypeComboBox.addItem(foodType);
        }   
    }
    
    public void fullInDataComboBox() {
        
        Date date = new Date();
        SimpleDateFormat  sdf = new SimpleDateFormat("yyyy/MM/dd");
        sdf.format(date);
        
        Calendar cal = Calendar.getInstance();
        for(int i = 0; i < 8; i++) {
            cal.add(Calendar.DATE, i); //next day
            cal.getTime(); // next day's date
            sdf.format(cal.getTime());
            dateJComboBox.addItem(sdf.format(cal.getTime()));
        }
        
        //dateJComboBox
    }
    
