    private void nextUserInfoJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextUserInfoJButtonActionPerformed
        // TODO add your handling code here:
        System.out.println("Current ID is " +  currentID);
        //System.out.println("Current Size is " +  userList.size());
        
        if(currentID == userList.size() - 1) {
            //System.out.println("Hit the end of the list, current ID is " +  currentID);
            updateInterface(userList.size() - 1);
            nextUserInfoJButton.setEnabled(false);
            perviousUserInfoJButton.setEnabled(true);
            //currentID = userList.size() - 1;
        } else {
            if(!userList.isEmpty() && userList.size() != currentID) {
                //System.out.println("Moving forward, current ID is " +  currentID);
                perviousUserInfoJButton.setEnabled(true);
                updateInterface(currentID);
                currentID++; 
            }
        }
    }//GEN-LAST:event_nextUserInfoJButtonActionPerformed

    private void perviousUserInfoJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_perviousUserInfoJButtonActionPerformed
        
        if(currentID == 0) {
            updateInterface(0);
            //System.out.println("Hit the end of the list, current ID is " +  currentID);
            nextUserInfoJButton.setEnabled(true);
            perviousUserInfoJButton.setEnabled(false);
            //currentID = 1;
        } else {
            if(!userList.isEmpty() || currentID != 0) {
                nextUserInfoJButton.setEnabled(true);
                //currentID--;
                //System.out.println("Moving backforward, current ID is " +  currentID);
                updateInterface(currentID);
                currentID--; 
            }
        }
    }//GEN-LAST:event_perviousUserInfoJButtonActionPerformed

