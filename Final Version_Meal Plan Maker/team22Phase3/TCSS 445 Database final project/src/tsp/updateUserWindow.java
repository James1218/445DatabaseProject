    private void confirmUpdateUserInfoJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmUpdateUserInfoJButtonActionPerformed
        String accountPW = accountPasswordJTextField.getText();
        String accountDName = accountDNameJTextField.getText();
        String accountDEmail = accountDEmailJTextField.getText();
        String accountDPhone = accountDPhoneJTextField.getText();
        String accountAddress = accountAddressJTextArea.getText();
        String accountName = accountNameJTextField.getText();
        
        boolean updateResult = false;
        
        updateResult = DatabaseAccess.updateUser(accountName, accountPW, accountDName,
                accountAddress,
                accountDPhone, accountDEmail);
                
        if(updateResult) {
            updateArrayList(accountName, accountPW, accountDName, accountAddress,
                accountDPhone, accountDEmail);
            JOptionPane.showMessageDialog(null, "User info updated!");
        } else {
            JOptionPane.showMessageDialog(null, "User info did not update!");
        }
        //JOptionPane.showMessageDialog(null, "Update successful!");
    }//GEN-LAST:event_confirmUpdateUserInfoJButtonActionPerformed

