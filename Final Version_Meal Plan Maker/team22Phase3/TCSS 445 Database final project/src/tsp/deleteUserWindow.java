    private void deleteUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteUserJButtonActionPerformed
        // TODO add your handling code here:
        if(!userList.isEmpty()) {
            String deleteAccountUserName = ((String)userNameJComboBox.getSelectedItem());
            String confirmMessage = "Would You Like to delete the user : " + deleteAccountUserName + "?";
            int dialogResult = JOptionPane.showConfirmDialog (null,
                    confirmMessage,
                    "Warning" ,
                    JOptionPane.YES_NO_OPTION);

            if(dialogResult == JOptionPane.YES_OPTION){
                userNameJComboBox.removeItemAt(userLocation);
                userList.remove(userLocation);
                DatabaseAccess.deleteUser(deleteAccountUserName);
                JOptionPane.showMessageDialog(null, "User list updated!");
                
                if(userList.isEmpty()) {
                    userInfoJTextArea.setText("There is no user in the user list");
                    deleteUserJButton.setEnabled(false);
                }
            }
        } else {
            userInfoJTextArea.setText("There is no user in the user list");
            deleteUserJButton.setEnabled(false);
        }
    }//GEN-LAST:event_deleteUserJButtonActionPerformed

