    private void userNameJComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userNameJComboBoxActionPerformed
        // TODO add your handling code here:
        String accountName = (String)userNameJComboBox.getSelectedItem();
        for(int i = 0; i < userList.size(); i++) {
            if(Objects.equals(accountName, userList.get(i).getAccount_Name())) {
                userInfoJTextArea.setText(userList.get(i).toString());
                userLocation = i;
                break;
            }
        }
    }//GEN-LAST:event_userNameJComboBoxActionPerformed

